-- Mostrar números de linea
vim.opt.number = true

-- Mostrar numero relativo de linea
vim.opt.relativenumber = true

-- Colores de la terminal
vim.opt.termguicolors = true

-- Compartir portapapeles con el OS
vim.opt.clipboard = 'unnamedplus'

-- Número de espacios de tabulador
vim.opt.tabstop = 4

-- Número de espacios automáticos
vim.opt.shiftwidth = 4

-- Número de espacios cuando se usa <BS> o <Del>
vim.opt.softtabstop = 4

-- ESPACIO como tecla líder
vim.g.mapleader = " "

-- Desactivar el ratón
vim.opt.mouse = ""

-- Mostrar linea de cursor
vim.opt.cursorline = true

